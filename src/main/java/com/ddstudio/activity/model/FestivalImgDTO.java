package com.ddstudio.activity.model;


public class FestivalImgDTO {

	private String festival_img_seq;
	private String img;
	private String festival_seq;

	public String getFestival_img_seq() {
		return festival_img_seq;
	}

	public void setFestival_img_seq(String festival_img_seq) {
		this.festival_img_seq = festival_img_seq;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getFestival_seq() {
		return festival_seq;
	}

	public void setFestival_seq(String festival_seq) {
		this.festival_seq = festival_seq;
	}
}
