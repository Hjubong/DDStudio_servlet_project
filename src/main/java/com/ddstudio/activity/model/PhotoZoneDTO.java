package com.ddstudio.activity.model;


public class PhotoZoneDTO {

	private String photozoen_seq;
	private String name;
	private String time;
	private String info;
	private String location_seq;

	public String getPhotozoen_seq() {
		return photozoen_seq;
	}

	public void setPhotozoen_seq(String photozoen_seq) {
		this.photozoen_seq = photozoen_seq;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public String getLocation_seq() {
		return location_seq;
	}

	public void setLocation_seq(String location_seq) {
		this.location_seq = location_seq;
	}
}
